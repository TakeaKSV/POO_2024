# Usando while
numero = 1
while numero <= 60:
    cuadrado = numero ** 2
    print(f"El cuadrado de {numero} es {cuadrado}")
    numero += 1
print("                                               ")

# Usando for
for numero in range(1, 61):
    cuadrado = numero ** 2
    print(f"El cuadrado de {numero} es {cuadrado}")