#Formas de concatenacion en python

nombre="Cesar Sarmiento"
especialidad="Payaso de mucha comedia"
carrera="Ingenieria en gestion y desarrollo de SW"

#1er forma

print("Mi nombre es" + nombre + "estoy en la especialidad de" 
      + especialidad + "en la carrera de" +carrera)

print("\n")

#2da forma
print("Mi nombre es",nombre," estoy en la especialidad de",especialidad,
      " en la carrera de",carrera)

print("\n")

#3ra forma
print(f"Mi nombre es ,{nombre} estoy en la especialidad de ,{especialidad}, en la carrera de {carrera}")

print("\n")

#4ta forma
print('Mi nombre es' )
